public class pwincess {
    private String name;
    private String kingdom;
    private int age;

    // public pwincess() {}

    public pwincess(String name, String kingdom, int age) {
        this.name = name;
        this.kingdom = kingdom;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getKingdom() {
        return kingdom;
    }

    public void setKingdom(String kingdom) {
        this.kingdom = kingdom;
    }

    
    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
